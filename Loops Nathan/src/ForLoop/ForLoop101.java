package ForLoop;

public class ForLoop101 
{
	public static void main(String[] args) 
	{
		//for(Initialize; Condition; Increment)
		//Start at 1; Count to 5; Increment by 1
		for(int i = 1; i <= 5; i++)
		{
			System.out.print(i+" ");
			
			//System.out.println("\n");
		}
	
		
		//Start at 5; Count to 1; Decrement by 1
		for(int i = 5; i >= 1; i--)
		{
			System.out.print(i+" ");
			
			//System.out.println("\n");
		}
	
	
		//Start at 5; Count to 25; Increment by 5
		for(int i = 5; i <= 25; i += 5)  //Increase by any no...... i += any no.
		{
			System.out.print(i+" ");
			
			//System.out.println("\n");
		}
	
	
	
	
	}









}
